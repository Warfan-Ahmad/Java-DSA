public class arrayTraversal {
    public static void main(String[] args) {
        int arr[] = {1,2,3,4,5};


        for(int i=0;i<arr.length;i++)
        {
            System.out.print(arr[i]);
        }

        for(int j=1;j<=arr.length-1; j++)
        {
            System.out.print(arr[j]);
        }
    
    
    }
}
